<h2 class="ui header">Questionnaire - Section 3 - How Much Can You Afford To Risk?</h2>
<p>
Risk capacity measures how much risk someone can afford to take. This is typically a calculation of existing assets, income,
expenses, future earning potential, time horizon and possibly age. Again see Hanna, Chen – <em>“Subjective and objective
risk tolerance: Implications for optimal portfolios. Financial Counseling and Planning”</em> – 1997 and Hanna, Waller, Finke
in <em>“The Concept of Risk Tolerance in Personal Financial Planning”</em> – 2008. In this section we ask 4 questions. The academic
literature on risk capacity is relatively light compared to risk tolerance but experience from clients, advisors, and regulators
help to form our questions.
</p>
<p>
9. <b>How long do you think you will remain invested before taking money out?</b>
</p>
<p>
<em>Possible Answers</em> – 0 - To 12 or more years
</p>
<p>
<b>Commentary</b> - This question is included because of academic research (see Hanna, Chen – <em>“Subjective and objective risk tolerance:
Implications for optimal portfolios. Financial Counseling and Planning”</em> – 1997) but mostly because of feedback from financial
advisors. They want to know if a client plans to sell assets relatively quickly. Using historical analysis it is difficult
to predict stock performance in the short-run (less than three years) but if a client plans to invest for decades an advisor
can be more confident about future returns. This will alter the investments that are recommended by an advisor and are thus
a crucial element in creating a financial plan.
</p>
<p>
10. <b>How secure are your current sources of income (e.g. job, investment property)?</b>
</p>
<p>
<em>Possible Answers</em> – Very Insecure, Insecure, Somewhat insecure, Not sure, Somewhat secure, Secure, Very secure
</p>
<p>
<b>Commentary</b> - Question 10 asks the client to assess the security of their income. Regulators like the UK’s Financial
Services Authority (now renamed Financial Conduct Authority) and the Securities and Exchange Board of India define risk
capacity as the “ability to absorb falls” in the value of investments that can have a “materially detrimental effect” on a
client’s “standard of living”. See Financial Services Authority – “Assessing Suitability” - 2011. It stands to reason income
insecurity is a major consideration when assessing a client’s capacity for loss. That is why we have included this question
in our questionnaire.
</p>
<p>
11. <b>How secure do you feel funding an unexpected event like a job loss or a large medical bill?</b>
</p>
<p>
<em>Possible Answers</em> – Very Insecure, Insecure, Somewhat insecure, Not sure, Somewhat secure, Secure, Very secure
</p>
<p>
<b>Commentary</b> - Similar to question 10 we are assessing a cleints’s ability to absorb losses.
</p>
<p>
12. <b>How would you describe your ability to save money?</b>
</p>
<p>
<em>Possible Answers</em> – Very poor, Poor, Somewhat poor, Not sure, Somewhat good, Good, Very good
</p>
<p>
<b>Commentary</b> - Again, similar to question 11 this question seeks to discover the client’s ability to absorb losses.
A high savings rate (income minus expenses) allows clients to absorb losses and re-invest to meet future financial goals.
</p>
