<div class="ui text container">
<h2 class="ui header">Questionnaire - Section 3 - How Much Financial Risk Can You Tolerate?</h2>
<p>
Risk tolerance measures how much risk someone is willing to take. In our questionnaire it includes risk aversion,
loss aversion and similar phrases but <b>not</b> risk capacity (that’s in our next section of questions). Risk tolerance
is a stable (in the client’s brain) psychological construct. Discussed by Hanna, Waller and Finke in
<em>“The Concept of Risk Tolerance in Personal Financial Planning”</em> – 2008 and Roszkowski –
<em>“Risk Perception and Risk Tolerance Changes Attributable to the 2008 Economic Crisis: A Subtle but Critical Difference”</em> - 2010.
This risk tolerance section of our questionnaire is a psychometric test. Meaning it measures psychological preferences.
</p>
<p>
The evidence shows that asking clients questions about loss aversion, past and future behavior is a good predictor of portfolio
preference and responses to market declines. See Guillemette, Finke, Gilliam – <em>“Risk Tolerance Questions to Best Determine
Client Portfolio Allocation Preferences”</em> – 2012. What is key, is having the right questions, in sufficient quantity that
elicit a behavioral response. See Grable, Lytton – <em>“Assessing The Concurrent Validity Of The SCF Risk Tolerance Question”</em> – 2012.
</p>
<p>
We’ve also included elements of Kahneman and Tversky’s – <em>“Prospect Theory: An Analysis of Decision under Risk”</em> - 1979.
In our questions we’ve gone to lengths to mitigate anchoring and appreciate that for most people losses feel much worse
than gains feel good. See Tversky and Kahneman – <em>“Advances in prospect theory: Cumulative representation of uncertainty”</em> – 1992.
Kahneman is now famous for winning the 2002 Nobel Prize in Economics. For those of you who enjoy Kahneman’s work we highly
encourage you to watch a discussion with Nassim Taleb on <a target="_blank" href="https://www.youtube.com/watch?v=MMBclvY_EMA">risk</a>.
</p>
<p>
In this risk tolerance section of our questionnaire we’ve included 10 questions. We’ve been inspired by the aforementioned academics
above as well as Rozkowski – <em>“The Survey of Financial Risk Tolerance”</em> – 1994 and Grable, Lytton – <em>“Financial risk tolerance revisited:
the development of a risk assessment instrument”</em> – 1999. We’ve also learned from institutions like Vanguard in their paper <em>“Investment
risk and financial advice”</em> – 2012.
</p>
<p>
Most importantly we’ve been listening to financial advisors and their clients for over four years. Building a risk profile
questionnaire is a practical exercise, not merely academic. Thousands of individuals have completed Pocket Risk questionnaires
and we’ve utilized this experience and data to build a robust product.
</p>
<p>
3. <b>If you were invested 60% in U.S. stocks and 40% in U.S. bonds during the 2008 financial crisis, your portfolio would have fallen 20.1%.
Your [x] investment would have been worth [y]. If a similar event happened again, what might you do with your investments after the
fall in value?</b>
</p>
<p><em>Possible Answers</em> – Sell everything, Sell a lot, Sell some, Neither buy nor sell, Buy a lot more, Buy as much as I can</p>
<p>
<b>Commentary</b> - This question is designed discover how a client would react in a downturn. Essentially dealing with
loss aversion and tackling the problem of the “dual self”. The battle between our rational and emotional decision-making.
See Fudenberg and Kline – <em>“A Dual-Self Model of Impulse Control”</em> – 2006.
</p>
<p>
The first part of the question is designed to mitigate anchoring to today (Tversky and Kahneman – <em>“Judgment Under Uncertainty:
Heuristics and Biases”</em> - 1973) by pushing the client to imagine a historical event. The question also uses numbers the client
has inputted into the questionnaire earlier on, to make the decision more emotionally significant.
</p>
<p>
4. <b>If your investments increased 12% (a to b) in year one, then 18% (b to c) in year two, what are you most likely to do in year three?</b>
</p>
<p>
<em>Possible Answers</em> – Sell everything, Sell a lot, Sell some, Neither buy nor sell, Buy a lot more, Buy as much as I can
</p>
<p>
<b>Commentary</b> – This question tests how clients feel on the upside. You might be wondering why we give 7 options for
many of the questions. This is to ensure variability in responses so we can better pinpoint someone’s risk tolerance score.
</p>
<p>5. <b>Have you ever sold investments solely because of a stock market decline?</b></p>
<p>
<em>Possible Answers</em> – Yes, I sold everything, Yes I sold a lot, Yes, I sold some, I neither bought nor sold,
No I bought some more, No I bought a lot more, No I bought as much as I could
</p>
<p>
<b>Commentary</b> - This question was included because advisors have told us they like to ask this question. From their
experience it has been a good predictor of future client behavior and an insight into their risk tolerance.
</p>
<p>
6. <b>The chart below is a representation of U.S. stock and bond market performance from 2005 to 2016 (with dividends and interest reinvested, excluding fees and taxes). Although past performance is no guarantee of future results, which portfolio best suits your desired level of risk and return?</b>
</p>
<p>
<img class="screen" src="<?php echo $uploads['url']; ?>/q6-screen.png" alt="Q6 screen">
</p>
<p><em>Possible Answers</em> – A, B, C, D, E, F, G</p>
<p>
<b>Commentary</b> - A serious challenge for advisors is assisting clients who are financially illiterate. A client’s lack of
knowledge and/or terminology to express themselves can result in miscommunication with an advisor. Some clients find
it difficult to accept risk because they don’t know the options. See Dow, Da Costa, Werlang – <em>“Uncertainty Aversion,
Risk Aversion, and the Optimal Choice of Portfolio”</em> – 1992. This question is designed to help clients know their options
(in a simple fashion) and choose what they think is an appropriate portfolio.
</p>
<p>
7. <b>To what extent do you agree with the following statements? - You have an excellent understanding of financial
and investment terms. - You are willing to invest in assets with a limited track record to get higher returns. -
You associate the word “risk” with “opportunity”.</b>
</p>
<p>
<em>Possible Answers</em> – Strongly Disagree, Disagree, Somewhat Disagree, Not Sure, Somewhat Agree, Agree, Strong Agree
</p>
<p>
<b>Commentary</b> - Question 7 is a series of 3 questions asking clients to agree or disagree to a number of statements
about their risk tolerance.
</p>
<p>
The first question asks clients about their level of financial education. Various papers and dissertations have supported
the assertion that more educated people have a higher risk tolerance. See Bucher-Koenen and Ziegelmeyer (2014) and Sahm –
<em>“Risk Tolerance and Asset Allocation”</em> – 2007. Thus we test for it the questionnaire.
</p>
<p>
The second question asks clients about their desire for upside, given the risks of investing in an asset with a limited track
record. The third question is looking for a visceral response to the word “risk” as an attempt to assess behavior during
a market downturn. Essentially, these questions are asking the client how much risk they are willing to take in different
ways to improve the reliability and validity of the final assessment.
</p>
<p>
8. <b>To what extent do you agree with the following statements? - You are more concerned with maximizing returns than minimizing
losses. - You would be comfortable investing in a new business venture. - You are comfortable with financial risk, knowing
there can be increases and decreases in the value of your investments.</b>
</p>
<p>
<em>Possible Answers</em> – Strongly Disagree, Disagree, Somewhat Disagree, Not Sure, Somewhat Agree, Agree, Strong Agree
</p>
<p>
<b>Commentary</b> - Question 8 is similar to question 7. It is a series of 3 questions asking clients to agree or
disagree to a number of statements about their risk tolerance.
</p>
