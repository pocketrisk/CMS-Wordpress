<h2 class="ui header">Questionnaire – Section 1 - What Are Your Financial Goals</h2>
<p>
The first section of the questionnaire reviews a client’s goals. Every professional advisor would agree that financial planning needs goals. It is our belief goals should be SMART - specific, measurable, assignable, realistic and time bound. This is based on George T. Doran’s original management paper <em>“There's a S.M.A.R.T. way to write management's goals and objectives.”</em>
</p>
<p>
We believe SMART goals lead to efficient and effective action. However, these can quickly get complex. We’ve allowed clients to state their goals in a simplistic fashion because they probably won’t know the answers to specific questions (e.g. the amount of income they want in retirement).
</p>
<p><b>Questions</b></p>
<p>1. <b>Rank your top three financial goals by clicking on the icons below</b><br><br>
<img class="screen" src="<?php echo $uploads['url']; ?>/q1-screen.png" alt="Q1 screen"></p>
<p>
<em>Possible Answers</em> – Wealth accumulation, Retirement, College Savings, Wealth preservation, Large purchase, More income, Passing on an inheritance, Other (please explain)
</p>
<p>
<b>Commentary</b> - We begin our questionnaire by reminding clients that the purpose of investing is a life outcome, not a rate of return. Given the wide range of backgrounds of people who complete our questionnaire we err on the side of simplicity. For those who select “Other” we ask them to explain further.
</p>
