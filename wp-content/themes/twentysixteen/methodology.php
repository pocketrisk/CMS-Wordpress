<div class="ui text container">
<h2 class="ui header">Questionnaire - Section 2 Continued – Your Financial Situation</h2>
<p>
13. <b>Approximately what percentages of your existing investments are in the following assets?</b>
</p>
<p>
<em>Possible Answers</em> – Stocks – 0%-100%, Bonds – 0%-100%, Cash – 0%-100%, Investment property – 0%-100%, Other – 0%-100%
</p>
<p>
<b>Commentary</b> - This question is designed to get a window into a clients risk composure and past decisions.
If market valuations are low and the client is entirely in cash, it could suggest they are fearful about investing.
Alternatively, a person close to retirement might be taking too much risk if they are 100% in stocks.
</p>
<p>
14. <b>Investors can suffer poor investment performance because of a desire to switch investments on a frequent basis.
This can happen when people overreact to good or bad news from the media and friends. How good are you at sticking with
your financial plans?</b>
</p>
<p>
<em>Possible Answers</em> – Very poor, Poor, Somewhat poor, Not sure, Somewhat good, Good, Very good
</p>
<p>
<b>Commentary</b> - Over time the role of financial advisors has shifted from recommending investments to client behavior
control. On average investors lose 1.6% annually because they tend to pull money out of equity mutual funds following a
signiﬁcant decline when equity valuations are most favorable, and conversely increase equity allocations following a recent
price increase when valuations are less favorable. See Friesen, Sapp – <em>“Mutual Fund Flows and Investor Returns: An Empirical
Examination of Fund Investor Timing Ability”</em> - 2007. Therefore advisors can provide significant value by controlling behavior.
</p>
<p>
Nick Murray a well-respected financial advisor coach says, <em>“that the dominant determinant of long-term, real-life return
is not investment performance but investor behavior. Second, that behavior modification ought to be, in and of itself,
an advisor's value proposition, because great behavioral advice is — at critical moments in an investor's lifetime — worth
so much more than the advisor can ever charge for it.“</em> – Behavioral Investment Counseling – 2008.
</p>
<p>
Given the research we have included questions on client behavior. We’ve included two questions to assess risk composure
(question 13) and risk perception (question 14)
</p>
<p>
Risk composure is the possibility that in a perceived crisis the client will behave significantly different to their rational
self and take action that could lock-in losses. It can be assessed based on a client’s past decisions.
</p>
<p>
Risk perception is a judgment by the client that the current economic environment is very risky, even if that judgment
is false. The media heavily influences this perception. A lack of financial literacy, education or experience is
typically to blame for these judgments.
</p>
<p>
Question 14 is based on research from Friesen and Sapp mentioned above as well as from Morningstar’s Managed Account Program
and Dalbar’s Quantitative Analysis Of Investor Behavior - 2015.
</p>
<p>
Pocket Risk has a customer who likes to ask his clients how often they watch CNBC and Bloomberg. He says, those who watch
such programs are the most difficult to manage because they are constantly reacting to news that doesn’t matter in the long-term.
</p>
<p>
15. <b>Anything else we should know about your financial situation (optional)?</b>
</p>
<p>
