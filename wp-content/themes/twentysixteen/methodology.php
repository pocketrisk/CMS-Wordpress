<h2>Summary</h2>
<div class="ui relaxed bulleted list">
<div class="item">
Pocket Risk is a risk questionnaire that assesses a client’s goals, risk tolerance,
risk capacity and behavioral biases. Each of these elements is scored separately. The overall
results can then be connected to an investment strategy.
</div>
<div class="item">
Questions based on a mix of Nobel Prize winning academic research about behavioral finance as
well as research on risk tolerance, risk capacity, psychometrics, and investor reactions
during market declines.
</div>
<div class="item">
15 questions. An online and pdf report presents the results.
</div>
<div class="item">
Built to comply with all major financial regulators including the SEC, FINRA, FCA, ESMA,
ASIC, IIROC, MFDA, and SEBI.
</div>
