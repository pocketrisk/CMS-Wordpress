<div class="description">
<p>
Suitability and risk profiling is overseen by the Australian Securities and Investments Commission (ASIC) and the Financial Service Ombudsman.
</p>
<p>
The focus for Australian regulators is that best interests have been applied by Financial Services Professional (FSPs). Professionals must <span class="quote">“ensure the financial products they recommend are suitable having regard to each client’s objectives, financial situation and needs. An important part of an FSPs assessment of a client’s objectives, financial situation and needs is the knowledge of the client’s <em>tolerance to risk</em>.”</span> The regulator goes a step further and states FSPs should <span class="quote">“educate their clients about risk and reward”</span>.
</p>
<p>
ASIC and the Financial Ombudsman are supportive of risk questionnaires but state FSPs should not be 100% dependent on their results. They should use their judgement in conjunction with a questionnaire.
</p>
<p>
Pocket Risk is compliant with Australian regulations.
</p>
