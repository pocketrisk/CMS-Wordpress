<div class="description">
<p>
Suitability and risk profiling is regulated by the Financial Conduct Authority (FCA) formerly the Financial Services Authority (FSA).
</p>
<p>
The UK has the most prescriptive suitability rules in the world. In 2011 the FSA (now FCA) released <span class="quote">“Assessing Suitability: Establishing The Risk A Customer Is Willing And Able To Take And Making A Suitable Investment Selection”</span>. The assessment found that most advisors were not properly assessing client suitability for investment. Their main findings were that…
</p>
<ol>
<li>Advisors were not diligently assessing risk tolerance.</li>
<li>Advisors who were assessing risk tolerance often missed risk capacity and goals.</li>
<li>Advisors were not assessing clients’ investment knowledge and experience.</li>
<li>Advisors were using poorly constructed questionnaires that could sway clients too far into aggressive risks.</li>
</ol>
<p>
Since this paper was released standards for suitability investment have increased. All UK advisors now have to provide a suitability report when recommending investments to clients.
</p>
<p>
Pocket Risk is compliant with UK regulations.
</p>
